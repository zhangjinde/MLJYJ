package cn.itcast.domain;

public class Remind {
	private String homeaddress;
	private String remindclass;
	private String phone;
	private String sex_name;
	
	private String xf_project;
	
	
	public String getXf_project() {
		return xf_project;
	}
	public void setXf_project(String xf_project) {
		this.xf_project = xf_project;
	}
	public String getHomeaddress() {
		return homeaddress;
	}
	public void setHomeaddress(String homeaddress) {
		this.homeaddress = homeaddress;
	}
	public String getRemindclass() {
		return remindclass;
	}
	public void setRemindclass(String remindclass) {
		this.remindclass = remindclass;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getSex_name() {
		return sex_name;
	}
	public void setSex_name(String sex_name) {
		this.sex_name = sex_name;
	}
	
}
