package cn.itcast.domain;

public class PhotoAlbumClass {
	private Integer pac_id;
	private String pac_name;
	private String pac_instructions;
	
	public Integer getPac_id() {
		return pac_id;
	}
	public void setPac_id(Integer pac_id) {
		this.pac_id = pac_id;
	}
	public String getPac_name() {
		return pac_name;
	}
	public void setPac_name(String pac_name) {
		this.pac_name = pac_name;
	}
	public String getPac_instructions() {
		return pac_instructions;
	}
	public void setPac_instructions(String pac_instructions) {
		this.pac_instructions = pac_instructions;
	}
	
}
