package cn.itcast.domain;

public class Studio {
	private Integer studio_id;
	private String studio_name;
	private String studio_instructions;
	private String studio_address;
	private String studio_phone;
	
	public Integer getStudio_id() {
		return studio_id;
	}
	public void setStudio_id(Integer studio_id) {
		this.studio_id = studio_id;
	}
	public String getStudio_name() {
		return studio_name;
	}
	public void setStudio_name(String studio_name) {
		this.studio_name = studio_name;
	}
	public String getStudio_instructions() {
		return studio_instructions;
	}
	public void setStudio_instructions(String studio_instructions) {
		this.studio_instructions = studio_instructions;
	}
	public String getStudio_address() {
		return studio_address;
	}
	public void setStudio_address(String studio_address) {
		this.studio_address = studio_address;
	}
	public String getStudio_phone() {
		return studio_phone;
	}
	public void setStudio_phone(String studio_phone) {
		this.studio_phone = studio_phone;
	}
	
}
